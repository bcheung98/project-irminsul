export const localMatNames = localMaterials.map((mat) => mat.tag);

export function groupedLocalMatNames(showUnreleased = false) {
    let materials;
    if (!showUnreleased) {
        materials = [...localMaterials].filter((mat) =>
            isUnreleasedContent(mat.release.version)
        );
    } else {
        materials = localMaterials;
    }
    const groupedMaterials: Record<string, string[]> = {};
    materials.forEach((mat) => (groupedMaterials[mat.source] = []));
    materials.forEach((mat) =>
        groupedMaterials[mat.source].push(mat.displayName)
    );
    return groupedMaterials;
}

export function getLocalMaterial({ id, tag }: { id?: string; tag: string }) {
    return localMaterials.find((mat) => mat.id === id || mat.tag === tag);
}
